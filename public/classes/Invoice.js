//classes
export class Invoice {
    //shorthand - muszą być podane modyfikatory innaczej nie zadziała
    constructor(client, details, amount) {
        this.client = client;
        this.details = details;
        this.amount = amount;
    }
    format() {
        return `${this.client} owes £${this.amount} for ${this.details}`;
    }
}
